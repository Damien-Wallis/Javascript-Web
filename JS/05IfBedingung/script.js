// Aufgabe 1
let number1 = 21;

// Wenn die Zahl kleiner ist als 20  gib aus "Mini"
if (number1 < 20) {
  console.log("Zahl 1: Mini");
}
// Wenn die Zahl zw. 20 und 50 ist gib aus "Medium"
else if (number1 >= 20 && number1 <= 50) {
  console.log("Zahl 1: Medium");
}
// Wenn die Zahl größer als 50 ist gib aus "Large"
else {
  console.log("Zahl 1: Large");
}

